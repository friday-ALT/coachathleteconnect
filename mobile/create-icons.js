const fs = require('fs');
const path = require('path');

// Create a minimal PNG file (1x1 green pixel)
const createPNG = (width, height) => {
  // This is a minimal valid PNG file structure
  const png = Buffer.from([
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D,
    0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
    0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53, 0xDE, 0x00, 0x00, 0x00,
    0x0C, 0x49, 0x44, 0x41, 0x54, 0x08, 0xD7, 0x63, 0x60, 0xA8, 0x64, 0x00,
    0x00, 0x00, 0x04, 0x00, 0x01, 0x5C, 0xCD, 0xFF, 0x69, 0x00, 0x00, 0x00,
    0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82
  ]);
  return png;
};

const assetsDir = path.join(__dirname, 'assets');

// Create all required icons
fs.writeFileSync(path.join(assetsDir, 'icon.png'), createPNG(1024, 1024));
fs.writeFileSync(path.join(assetsDir, 'adaptive-icon.png'), createPNG(1024, 1024));
fs.writeFileSync(path.join(assetsDir, 'favicon.png'), createPNG(48, 48));
fs.writeFileSync(path.join(assetsDir, 'splash.png'), createPNG(1242, 2688));

console.log('Icons created successfully!');
